package SHARAD;

public class SwapTwoNum {
	 public static void main(String[] args) {  
	       int x=10, y=20, t;   
	   
	        System.out.println("before swapping numbers:x= "+x +" y= "+ y);  
	       
	       t = x;  
	       x = y;  
	       y = t;  
	       System.out.println("After swapping:x= "+x +" y=  " + y);  
	       System.out.println( );  
	    }    
}
